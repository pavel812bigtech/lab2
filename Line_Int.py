# =============================================
# ФУНКЦИЯ ЛИНЕЙНОЙ ИНТЕРПОЛЯЦИИ
# =============================================

def linear_interpolation(x1, y1, x2, y2, x):
    """
    Линейная интерполяция между двумя точками

    x1, x2 — координаты по оси X (узлы интерполяции)
    y1, y2 — значения функции в этих узлах
    x  — точка, в которой хотим найти значение
    """
    # Проверка, что x находится между x1 и x2
    if x1 == x2:
        print("⚠️ Ошибка: x1 == x2 (деление на ноль)")
        return y1

    # Основная формула
    y = y1 + (y2 - y1) * (x - x1) / (x2 - x1)
    return y


