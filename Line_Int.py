# ТЕСТИРОВАНИЕ ФУНКЦИИ
# =============================================

print("Тесты линейной интерполяции:\n")

# Тест 1 — простой
print("Тест 1: x1=0, y1=0, x2=10, y2=10")
print("Значение в x=5  →", linear_interpolation(0, 0, 10, 10, 5))
print("Значение в x=3  →", linear_interpolation(0, 0, 10, 10, 3))
print("Значение в x=7  →", linear_interpolation(0, 0, 10, 10, 7))

# Тест 2 — другие значения
print("\nТест 2: x1=2, y1=4, x2=8, y2=10")
print("Значение в x=5  →", linear_interpolation(2, 4, 8, 10, 5))
print("Значение в x=4  →", linear_interpolation(2, 4, 8, 10, 4))

# Тест 3 — нецелые числа
print("\nТест 3: x1=0, y1=0, x2=1, y2=255")
print("Значение в x=0.5 →", linear_interpolation(0, 0, 1, 255, 0.5))

