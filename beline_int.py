def linear_interpolation(x1, y1, x2, y2, x):
    if abs(x2 - x1) < 1e-9:
        return y1
    return y1 + (y2 - y1) * (x - x1) / (x2 - x1)



def bilinear_interpolation(x1, y1, x2, y2,
                           z11, z12, z21, z22,
                           x, y):
    """
    Билинейная интерполяция

    Узлы:
        (x1,y1) -- z11     (x2,y1) -- z21
           |                   |
        (x1,y2) -- z12     (x2,y2) -- z22

    x ∈ [x1, x2], y ∈ [y1, y2]
    """

    # Проверки
    if x < x1 or x > x2 or y < y1 or y > y2:
        print(f"Предупреждение: точка ({x}, {y}) выходит за пределы квадрата!")

    # Вычисляем относительные расстояния
    dx = (x - x1) / (x2 - x1) if abs(x2 - x1) > 1e-9 else 0
    dy = (y - y1) / (y2 - y1) if abs(y2 - y1) > 1e-9 else 0

    # Основная формула
    z = (z11 * (1 - dx) * (1 - dy) +
         z12 * (1 - dx) * dy +
         z21 * dx * (1 - dy) +
         z22 * dx * dy)

    return z


